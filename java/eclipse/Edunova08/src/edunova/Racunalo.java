package edunova;

public class Racunalo {
	private String naziv;
	private String boja;
	private String operativniSustav;
	private float procesor;
	private boolean touchscreen;
	public String getNaziv() {
		return naziv;
	}
	public void setNaziv(String naziv) {
		this.naziv = naziv;
	}
	public String getBoja() {
		return boja;
	}
	public void setBoja(String boja) {
		this.boja = boja;
	}
	public String getOperativniSustav() {
		return operativniSustav;
	}
	public void setOperativniSustav(String operativniSustav) {
		this.operativniSustav = operativniSustav;
	}
	public float getProcesor() {
		return procesor;
	}
	public void setProcesor(float procesor) {
		this.procesor = procesor;
	}
	public boolean isTouchscreen() {
		return touchscreen;
	}
	public void setTouchscreen(boolean touchscreen) {
		this.touchscreen = touchscreen;
	}
	
	

}
