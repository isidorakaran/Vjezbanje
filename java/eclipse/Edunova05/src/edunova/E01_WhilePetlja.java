package edunova;

public class E01_WhilePetlja {
public static void main(String[] args) {
	while(true) {
		break;
	}
	
	boolean uvjet=false;
	while(uvjet) {
		System.out.println("Osijek");
	}
	
	for(int i=0;i<0;) {
		System.out.println("neće ući");
	}
	
	int broj=0;
	while(broj<10) {
		System.out.println(++broj);
		
	}
}
}
